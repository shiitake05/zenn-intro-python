# 等しい
is_equal = 5 == 2
print("5 == 2:", is_equal)  # 出力: False

# 等しくない
is_not_equal = 5 != 2
print("5 != 2:", is_not_equal)  # 出力: True

# より大きい
is_greater = 5 > 2
print("5 > 2:", is_greater)  # 出力: True

# より小さい
is_less = 5 < 2
print("5 < 2:", is_less)  # 出力: False

# 以上
is_greater_or_equal = 5 >= 2
print("5 >= 2:", is_greater_or_equal)  # 出力: True

# 以下
is_less_or_equal = 5 <= 2
print("5 <= 2:", is_less_or_equal)  # 出力: False
